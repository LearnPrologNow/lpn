Prolog est un langage de programmation tout &agrave; fait original. &laquo;Prolog&raquo;
est l'abr&eacute;viation de &laquo;Programmation logique&raquo;, et le lien qu'il
entretient avec la logique est ce qui lui donne sa sp&eacute;cificit&eacute;. Au
coeur de Prolog, r&eacute;side une id&eacute;e surprenante : ne pas dire &agrave;
l'ordinateur ce qu'il doit faire, mais lui d&eacute;crire des situations, et
lui faire faire des calculs en lui posant des questions. Prolog
d&eacute;duira alors logiquement de nouveaux faits concernant les situations,
et donnera en r&eacute;ponse ses d&eacute;ductions. Pourquoi apprendre Prolog ? Tout
d'abord, l'aspect &laquo;dis-moi quel est ton probl&egrave;me au lieu de me dire
comment le r&eacute;soudre&raquo; de son approche en fait un langage de tr&egrave;s haut
niveau, adapt&eacute; aux applications riches en connaissances comme
l'intelligence artificielle, la linguistique informatique, ou le web
s&eacute;mantique. En &eacute;tudiant Prolog, on arrive &agrave; une meilleure
compr&eacute;hension de la fa&ccedil;on dont des t&acirc;ches sophistiqu&eacute;es peuvent &ecirc;tre
effectu&eacute;es informatiquement.  
</p>
