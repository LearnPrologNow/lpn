
<p>
Several Prolog environments are available, and probably the best idea
is simply to google what's available. But we list here some of the
more widely used systems. Or go to the <a href="http://www.logic.at/prolog/faq/faq.html">comp.lang.prolog FAQ</a> for a more complete list.
</p>

<table>
<tr>
<td style="font-size:100%;" class="emph" valign="top">SWI-Prolog</td>
<td>
      A Free Software Prolog environment, licensed under the Lesser GNU public
      license.  This popular interpreter was developed by Jan
      Wielemaker. This is the interpreter we used while developing this book.<br>
      <a href="http://www.swi-prolog.org/">http://www.swi-prolog.org/</a>
</td></tr>
<tr><td style="font-size:100%;" class="emph" valign="top">SICStus Prolog</td>
<td>
      Industrial strength Prolog environment from the Swedish Institute of
      Computer Science.<br>
      <a href="http://www.sics.se/sicstus/">http://www.sics.se/sicstus/</a>
</td></tr>
<tr><td style="font-size:100%;" class="emph" valign="top">GNU Prolog</td>
<td>
Another more widely used free Prolog compiler developed by Daniel Diaz.
<a href="http://www.gprolog.org/">http://www.gprolog.org</a>
</td></tr>
<tr><td style="font-size:100%;" class="emph" valign="top">YAP Prolog</td>
<td>
      A Prolog compiler developed at the Universidade do Porto and Universidade
      Federa do Rio de Janeiro.  Free for use in academic environments.<br>
      <a href="http://www.ncc.up.pt/~vsc/Yap/">http://www.ncc.up.pt/~vsc/Yap/</a>
</td></tr>
<tr><td style="font-size:100%;" class="emph" valign="top">Strawberry Prolog</td>
<td>
Strawberry Prolog is specially designed for education.
It works with Windows Vista/XP as well as Unix/Linux.
      It is developed at the Bulgarian Academy of Sciences and can be downloaded from
      <a href="http://www.dobrev.com/">http://www.dobrev.com/</a>
</td></tr>

</table>
