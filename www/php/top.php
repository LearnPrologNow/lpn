<p>
Learn Prolog Now! is an introductory course to programming in <a
href="lpnpage.php?pageid=links">Prolog</a>. The <a
href="lpnpage.php?pageid=online">online version</a> has been available
since 2001, and now there is also a throughly revised version
available in <a href="lpnpage.php?pageid=handheld">book form</a>.
</p>

<p>
We wanted to do two things with this course. First, we wanted to
provide a text that was relatively self contained, a text that would
permit someone with little or no knowledge of computing to pick up the
basics of Prolog with the minimum of fuss. We also wanted the text to
be clear enough to make it useful for self study. We believe that if
you read the text, and do the associated exercises, you will gain a
useful partial entry to the world of Prolog.
</p>

<p> 
But only a partial entry, and this brings us to our second point. We
want to emphasize the practical aspects of Prolog. Prolog is something
you do. You can't learn a programming language simply by reading about
it, and if you really want to get the most out of this course, we
strongly advise you to get hold of a Prolog interpreter (you'll find
pointers to some nice ones on this website) and work through all the
Practical Sessions that we provide. And of course, don't stop with
what we provide. The more you program, the better you'll get....
</p>

<p> We hope you enjoy the course. And whether you're using this book
to teach yourself Prolog, or you're using it as the basis for teaching
others, we would like to hear from you. Please <a
href="lpnpage.php?pageid=contact">send us</a> any comments/corrections
you have so that we can take them into account in later versions.
</p>
